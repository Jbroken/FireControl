package com.fire.po;

public class PoliceStInfo {
	private String time;
	
	private String tablesum;
	
	private String policeStation;
	
	private String coverage;
	
	private Integer unitsum;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getTablesum() {
		return tablesum;
	}

	public void setTablesum(String tablesum) {
		this.tablesum = tablesum;
	}

	public String getPoliceStation() {
		return policeStation;
	}

	public void setPoliceStation(String policeStation) {
		this.policeStation = policeStation;
	}
	
	public String getCoverage() {
		return coverage;
	}

	public void setCoverage(String coverage) {
		this.coverage = coverage;
	}

	public Integer getUnitsum() {
		return unitsum;
	}

	public void setUnitsum(Integer unitsum) {
		this.unitsum = unitsum;
	}
	
}
